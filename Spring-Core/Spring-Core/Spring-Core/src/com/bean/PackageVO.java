package com.bean;

public class PackageVO {
	
	private String package_id;
	private String package_name;
	private int no_of_nights;
	private String room_type;
	private float base_tariff;
	private int no_of_adults;
	private int no_of_kids;
	
	
	public String getPackage_id() {
		return package_id;
	}
	public void setPackage_id(String package_id) {
		this.package_id = package_id;
	}
	public String getPackage_name() {
		return package_name;
	}
	public void setPackage_name(String package_name) {
		this.package_name = package_name;
	}
	public int getNo_of_nights() {
		return no_of_nights;
	}
	public void setNo_of_nights(int no_of_nights) {
		this.no_of_nights = no_of_nights;
	}
	public String getRoom_type() {
		return room_type;
	}
	public void setRoom_type(String room_type) {
		this.room_type = room_type;
	}
	public float getBase_tariff() {
		return base_tariff;
	}
	public void setBase_tariff(float base_tariff) {
		this.base_tariff = base_tariff;
	}
	public int getNo_of_adults() {
		return no_of_adults;
	}
	public void setNo_of_adults(int no_of_adults) {
		this.no_of_adults = no_of_adults;
	}
	public int getNo_of_kids() {
		return no_of_kids;
	}
	public void setNo_of_kids(int no_of_kids) {
		this.no_of_kids = no_of_kids;
	}
	
	

}
